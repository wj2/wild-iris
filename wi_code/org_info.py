glossary = c.OrderedDict()
glossary['annual'] = """
A plant that completes its life cycle in one growing season
"""
glossary['biennial'] = """
A plant that flowers, seeds, and dies in the second season after germination 
and produces only stems, roots, and leaves in the first season.
"""
glossary['perennial'] = """
A plant that lives for at least three seasons -- woody perennials leave woody 
stems at their base outside the growing season, while herbaceous perennials leave
nothing above ground.
"""
glossary['herbaceous'] = """
A plant with no persistent woody stems above ground (this includes most 
perennials and almost all annuals and biennials)
"""
glossary['woody'] = """
A plant with persistent woody stems above ground, with subcategories including
trees, shrubs, and lianas.
"""
glossary['tree'] = """
A perennial woody plant with an elongated trunk or stem.
"""
glossary['shrub'] = """
A small to medium-sized perennial woody plant. Smaller shrubs are termed
subshrubs. Shrubs with all of their woody stems lying on or close to 
the ground are termed prostrate shrubs.
"""
glossary['deciduous'] = """
A plant that loses its leaves annually at the end of the growing season.
Plants that lose only some of their leaves are referred to as semi-deciduous 
(somewhat interchangeable with semi-evergreen).
"""
glossary['evergreen'] = """
A plant that retains its leaves at the end of the growing season, and loses some
leaves regularly throughout the year. PLants that lose only some of their leaves
are referred to as semi-evergreen (somewhat interchangeable with semi-deciduous).
"""
glossary['bulb'] = """
A food storage organ, usually underground, that is used to sustain the plant 
through its dormant period (there are many variations on bulbs observed 
botanically, we have followed the horticultural convention and elided these
distinctions). A plant with a bulb is often referred to simply as a bulb. 
"""
glossary['vine'] = """
A plant that tends to grow using stems, lianas, or runners that climb other 
nearby plants or structures.
"""



